#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	j;
	int	l;

	i = '0';
	j = '1';
	l = '2';
	while (i++ <= '7')
	{
		while (j++ <= '8')
		{
			while (l++ <= '9')
			{
				write (1, &i, 1);
				write (1, &j, 1);
				write (1, &l, 1);
				write (1, ", ", 2);
			}
			l = j + 1;
		}
		j = i + 1;
	}
	write(1, "789", 3);
}
